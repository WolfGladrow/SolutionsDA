# EXERCISE & SOLUTION: Plot f(x) = tanh (3 * x - 6)
#      over the range $0 \le x \le 4$ (12/2016)
x = seq(-0,4,0.01)
y = tanh(3*x-6)
x0 = 2.1; sigmax = 0.5; y0 = tanh(3*x0-6)
xp = c(x0-sigmax,x0+sigmax); yp = c(y0,y0)
# png('tanh161204.png',width=16,height=16,units='cm',res=300)
plot(x,y,type='l',lwd=3,col='black',xlab='x',ylab='tanh(3x-6)',las=1,cex=0.4,cex.lab=1.5)
points(x0,y0,col='red',lwd=5,cex=1)
lines(xp,yp,col='red',lwd=2,lty=3)
